// fill() method modifies an array by filling all its elements with a static value from a start index to an end index (end not included).

let array = [1, 2, 3, 4, 5];
console.log("Original Array:", array);

// Fill the entire array with 0
array.fill(0);
console.log("Array after fill(0):", array);
// Output: [0, 0, 0, 0, 0]
